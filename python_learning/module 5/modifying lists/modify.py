# modifying lists change an item 
numbers = [1, 2, 3, 4, 5]
numbers[2] = 99
print(numbers) 

# another example of modifying lists
fruits = ["apple", "banana", "cherry"]
# changing the second element to "orange"
fruits[1] = "orange"
print(fruits)

# another example
num = [10, 20, 30, 40]
insert_index = 2
num.insert(insert_index, 35)
print(num)


