# python follows the standard math rule (BODMAS / PEMDAS) for operator precedence.

print(2 + 3 * 3) # 11 (multiplication is performed before addition)
print((2 + 3) * 4) #20 (parenthesis first: 5*4)
print(10 - 2 * 3) # 4 (2*3=6, then 10-6
print(2 ** 3 ** 2) # 512 (right to left: 3**2=9, then 2**9 )
 
